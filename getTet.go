
package main

func getTet(block []string) [][]rune {
    for _, figure := range allTets {
        n := len(figure)
        m := len(figure[0])


        for i := 0; i+n-1 < 4; i++ {

            for j := 0; j+m-1 < 4; j++ {
                newBlock := [][]rune{}
                for k := 0; k < n; k++ {
                     newBlock=append(newBlock, []rune(block[i+k][j:j+m] ))
                }
                if compare(newBlock, figure) {
                    return  newBlock
                }
            }
        }
    }
    return nil
}

func compare(a, b[][]rune) bool {
    for i, c := range a {
        for j := range c {
            if a[i][j] != b[i][j] {
                return false
            }
        }
    }
    return true
}

